    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(filePath.equals("")){
            JOptionPane.showMessageDialog(null, "请指定源文件夹！");
        }else if(garbagePath.equals("")){
            JOptionPane.showMessageDialog(null, "请指定回收站文件夹！");
        }else{
            File fmain = new File(filePath);
            imageFile = fmain.listFiles();//get the pathes of all pics
            imageFileLength=imageFile.length;
            ImageMove im =new ImageMove();
            jTextArea1.append("\n\n进行第"+count+"次对比筛选...");
            count++;
            //find max size 
            int maxPix =0 ;
            for (File flagFile : imageFile) {
                try {
                    BufferedImage flagImage = ImageIO.read(flagFile);
                    int flagPix =flagImage.getHeight()*flagImage.getWidth();
                    if(maxPix < flagPix){
                        maxPix=flagPix;
                    }
                    flagImage.flush();
                }catch(IOException e){
                    System.out.println(e.toString());
                }
            }
            //delete smaller image
            for (File flagFile : imageFile) {
                try {
                    BufferedImage flagImage = ImageIO.read(flagFile);
                    int flagPix =flagImage.getHeight()*flagImage.getWidth();
                    if(maxPix > flagPix){
                        jTextArea1.append("\n\n"+flagFile.getName().toString()+"图像分辨率不足，已放入回收站。");//
                        im.moveImage(flagFile, garbagePath);
                        flagFile.delete();
                        imageFile = fmain.listFiles();
                        imageFileLength--;
                    }
                    flagImage.flush();
                }catch(IOException e){
                    System.out.println(e.toString());
                }
            }
            //find similar image and delete 
            try {
                for (int i = 0;i < imageFileLength-1;i++) {
                    BufferedImage flagImageI = ImageIO.read(imageFile[i]);
                    for (int j = i+1;j < imageFileLength;j++){
                        BufferedImage flagImageJ = ImageIO.read(imageFile[j]);
                        ImageComparer ic =new ImageComparer(flagImageI, flagImageJ);
                        jTextArea1.append("\n\n图像:"+imageFile[i].getName().toString()+"与图像："
                                +imageFile[j].getName().toString()+"进行比对。"+"\n相似度为:"+ic.modelMatch()+"。");//
                        if(ic.modelMatch()>value){
                            jTextArea1.append("\n已放入回收站。");
                            im.moveImage(imageFile[j], garbagePath);
                            imageFile[j].delete();
                            imageFile = fmain.listFiles();
                            imageFileLength--;
                            j--;
                        }
                        flagImageI.flush();
                        flagImageJ.flush();
                    }
                }
                JOptionPane.showMessageDialog(null, "筛选成功！");
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        JFileChooser jFileChooser =new JFileChooser();//source fold path
        jFileChooser.setMultiSelectionEnabled(false);
        jFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int res = jFileChooser.showDialog(new JFrame(), "打开文件");
        if(res == JFileChooser.APPROVE_OPTION){
            filePath=jFileChooser.getSelectedFile().getPath().toString();
            jTextField1.setText(filePath);
            jFileChooser.approveSelection();
            JOptionPane.showMessageDialog(null,"提示：相似度最好大于80！");
        }else{
            jFileChooser.cancelSelection();
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "请重新尝试！");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

