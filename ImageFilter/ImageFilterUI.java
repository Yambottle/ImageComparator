    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(garbagePath.equals("")){
            JOptionPane.showMessageDialog(null, "请指定回收站文件夹！");
        }else{
        File fmain = new File(garbagePath);
        File[] garbageFile = fmain.listFiles();
        ImageMove im =new ImageMove();
        for(File flagGarbage:garbageFile){
            if(filePath.equals(""))JOptionPane.showMessageDialog(null, "请指定源文件夹！");
            im.moveImage(flagGarbage, filePath);
            flagGarbage.delete();
        }
        JOptionPane.showMessageDialog(null, "已还原回收站！");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

